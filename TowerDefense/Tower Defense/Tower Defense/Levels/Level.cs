        private List<Texture2D> tileTextures = new List<Texture2D>();

        //Access to the list in Game1
        public void AddTexture(Texture2D texture)
        {
            tileTextures.Add(texture);
        }

        //Check the Height and Width of the map
        public int Width { get { return map.GetLength(1); } }

        public int Height { get { return map.GetLength(0); } }

        //Returns the index of the cell
        public int GetIndex(int cellX, int cellY)
        {
            if (cellX < 0 || cellX > Width - 1 || cellY < 0 || cellY > Height - 1)
                return 0;

            return map[cellY, cellX];
        }

        //Draw function
        public void Draw(SpriteBatch batch)
        {
            for (int x = 0; x < Width; x++)
            {
                for (int y = 0; y < Height; y++)
                {
                    //Map the 1s and 0s to the correct textures
                    int textureIndex = map[y, x];
                    if (textureIndex == -1)
                        continue;
                    if (textureIndex == 1)
                    {
                        if(Main.levelSelect.Equals("Level 1"))
                        { Texture2D texture = tileTextures[0]; 
                            batch.Draw(texture, new Rectangle( x * 32, y * 32, 32, 32), Color.White); }
                        if (Main.levelSelect.Equals("Level 2"))
                        { Texture2D texture = tileTextures[1];
                            batch.Draw(texture, new Rectangle( x * 32, y * 32, 32, 32), Color.White); }
                        if (Main.levelSelect.Equals("Level 3"))
                        { Texture2D texture = tileTextures[2];
                            batch.Draw(texture, new Rectangle( x * 32, y * 32, 32, 32), Color.White); }
                        if (Main.levelSelect.Equals("Level 4"))
                        { Texture2D texture = tileTextures[3];
                            batch.Draw(texture, new Rectangle( x * 32, y * 32, 32, 32), Color.White); }
                        if (Main.levelSelect.Equals("Level 5"))
                        { Texture2D texture = tileTextures[4];
                            batch.Draw(texture, new Rectangle( x * 32, y * 32, 32, 32), Color.White); }
                    }
                }
            }
        }
